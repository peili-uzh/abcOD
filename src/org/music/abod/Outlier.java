package org.music.abod;

public class Outlier {
    int maxOutlierCount;
    int incSeriesCount;
    int decSeriesCount;

    public int getMaxOutlierCount() {
        return maxOutlierCount;
    }

    public void setMaxOutlierCount(int maxOutlierCount) {
        this.maxOutlierCount = maxOutlierCount;
    }

    public int getIncSeriesCount() {
        return incSeriesCount;
    }

    public void setIncSeriesCount(int incSeriesCount) {
        this.incSeriesCount = incSeriesCount;
    }

    public int getDecSeriesCount() {
        return decSeriesCount;
    }

    public void setDecSeriesCount(int decSeriesCount) {
        this.decSeriesCount = decSeriesCount;
    }
}
